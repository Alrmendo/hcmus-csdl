--FROM GIAOVIEN GV , BOMON BM ON GV.MABM = BM.MABM
FROM GIAOVIEN GV, BOMON BM
WHERE GV.HOTEN LIKE N'NGUYỄN %' AND GV.LUONG > 2000 OR (BM.TRUONGBM = GV.MAGV AND YEAR (BM.NGAYNHANCHUC) > 1995) 


